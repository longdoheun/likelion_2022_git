let animal = "dog";

switch (animal) {
    case 'cat':
        console.log("yaong")
        break;
    case 'dog':
        console.log("meong")
        break;
    case 'cow':
        console.log('eummae')
        break;
    default:
        console.log('no corresponding sound')
        break;
}
